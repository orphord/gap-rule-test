package com.orford.gapruletest.model;

public class Campsite {
	private Integer id;
	private String name;
	/**
	 * @return the campsite id
	 */
	public Integer getId() {
		return this.id;
	}
	/**
	 * @param _id the campsite id to set
	 */
	public void setId(Integer _id) {
		this.id = _id;
	}
	/**
	 * @return the campsite name
	 */
	public String getName() {
		return this.name;
	}
	/**
	 * @param _name the campsite name to set
	 */
	public void setName(String _name) {
		this.name = _name;
	}
	
	
}
