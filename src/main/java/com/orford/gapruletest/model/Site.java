package com.orford.gapruletest.model;

public class Site {
	private Integer id;
	private String name;
	/**
	 * @return the site id
	 */
	public Integer getId() {
		return this.id;
	}
	/**
	 * @param _id the site id to set
	 */
	public void setId(Integer _id) {
		this.id = _id;
	}
	/**
	 * @return the site name
	 */
	public String getName() {
		return this.name;
	}
	/**
	 * @param _name the site name to set
	 */
	public void setName(String _name) {
		this.name = _name;
	}
	
	
}
